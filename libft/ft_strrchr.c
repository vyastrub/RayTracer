#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*a;
	int		i;

	i = 0;
	a = (char*)s;
	while (*a)
	{
		a++;
		i++;
	}
	while (i-- >= 0)
	{
		if (*a == c)
			return (a++);
		a--;
	}
	return (NULL);
}
