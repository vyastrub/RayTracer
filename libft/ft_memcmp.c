#include <string.h>

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*str1;
	unsigned char	*str2;

	str1 = (unsigned char*)s1;
	str2 = (unsigned char*)s2;
	i = 0;
	if (n == 0)
		return (0);
	while (str1[i] == str2[i] && --n > 0)
		i++;
	if (str1[i] != str2[i])
		return ((int)(str1[i] - str2[i]));
	return (0);
}
