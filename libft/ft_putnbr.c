#include <unistd.h>

void	ft_putnbr(int n)
{
	char	s[10];
	int		c;

	if (n == 0)
		write(1, "0", 1);
	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			n = -n;
			write(1, "-", 1);
		}
		c = 0;
		while (n > 0)
		{
			s[c++] = n % 10 + 48;
			n = n / 10;
		}
		while (--c >= 0)
			write(1, &s[c], 1);
	}
}
