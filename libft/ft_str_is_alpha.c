int		ft_str_is_alpha(char *str)
{
	if (*str == '\0')
		return (1);
	while (*str)
	{
		if (*str >= 0 && *str <= 64)
			return (0);
		else if (*str >= 91 && *str <= 96)
			return (0);
		else if (*str >= 123 && *str <= 127)
			return (0);
		str++;
	}
	return (1);
}
