int		ft_atoi(const char *str)
{
	long int	n;
	int			sign;

	n = 0;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-')
	{
		sign = -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
		n = n * 10 + *str++ - '0';
	if (sign < 0)
		n = n * sign;
	return ((int)n);
}
