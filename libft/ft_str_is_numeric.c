int		ft_str_is_numeric(char *str)
{
	if (*str == '\0')
		return (1);
	while (*str)
	{
		if ((*str >= 0 && *str <= 47) || (*str >= 58 && *str <= 127))
			return (0);
		str++;
	}
	return (1);
}
